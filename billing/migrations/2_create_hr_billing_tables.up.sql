-- =============================================================
-- Migration 2: HR / Billing tables
--   customers      – client details + billing_months_per_year (BMPY)
--   employees      – deployed staff, linked to a customer
--   salaries       – monthly salary per employee per period
--   billing_records– monthly rate + annual billing per employee per year
--
-- ANB (Arab National Bank) BMPY = 11 (no vacation-month billing).
-- All other clients default to 12.
-- Syed Faraz Shah (Riyad Bank) has an individual billing_months_override = 11.
-- =============================================================

-- ----------------------------------------------------------
-- customers
-- ----------------------------------------------------------
CREATE TABLE customers (
  id                     UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name                   TEXT NOT NULL,
  short_name             TEXT UNIQUE NOT NULL,
  billing_months_per_year INTEGER NOT NULL DEFAULT 12
                           CHECK (billing_months_per_year BETWEEN 1 AND 12),
  notes                  TEXT,
  created_at             TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- ----------------------------------------------------------
-- employees
-- ----------------------------------------------------------
CREATE TABLE employees (
  id                     UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  serial_no              INTEGER,
  name                   TEXT NOT NULL,
  position               TEXT NOT NULL,
  customer_id            UUID NOT NULL REFERENCES customers(id) ON DELETE RESTRICT,
  -- Overrides customer BMPY for this individual (e.g. mid-year joiners)
  billing_months_override INTEGER
                           CHECK (billing_months_override BETWEEN 1 AND 12),
  created_at             TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_employees_customer ON employees(customer_id);

-- ----------------------------------------------------------
-- salaries  (monthly salary per employee per month/year)
-- ----------------------------------------------------------
CREATE TABLE salaries (
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  employee_id     UUID NOT NULL REFERENCES employees(id) ON DELETE RESTRICT,
  customer_id     UUID NOT NULL REFERENCES customers(id) ON DELETE RESTRICT,
  monthly_amount  NUMERIC(12,2) NOT NULL,
  effective_month INTEGER NOT NULL CHECK (effective_month BETWEEN 1 AND 12),
  effective_year  INTEGER NOT NULL,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (employee_id, effective_month, effective_year)
);

CREATE INDEX idx_salaries_employee   ON salaries(employee_id);
CREATE INDEX idx_salaries_customer   ON salaries(customer_id);
CREATE INDEX idx_salaries_period     ON salaries(effective_year, effective_month);

-- ----------------------------------------------------------
-- billing_records  (billing rate per employee per year)
-- ----------------------------------------------------------
CREATE TABLE billing_records (
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  employee_id     UUID NOT NULL REFERENCES employees(id) ON DELETE RESTRICT,
  customer_id     UUID NOT NULL REFERENCES customers(id) ON DELETE RESTRICT,
  monthly_rate    NUMERIC(12,2) NOT NULL,
  billing_months  INTEGER NOT NULL CHECK (billing_months BETWEEN 1 AND 12),
  annual_amount   NUMERIC(12,2) NOT NULL,
  billing_year    INTEGER NOT NULL,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (employee_id, billing_year)
);

CREATE INDEX idx_billing_employee    ON billing_records(employee_id);
CREATE INDEX idx_billing_customer    ON billing_records(customer_id);
CREATE INDEX idx_billing_year        ON billing_records(billing_year);

